package entradas;

import java.util.Scanner;

public class ScanOutrosTipos {
	public static void main(String[] args) {
		
	
	Scanner ler = new Scanner(System.in);
	
	short num2 = ler.nextShort();
	System.out.println(num2); 
	
	Long num3 = ler.nextLong(); 
	System.out.println(num3); 
	
	//short - um inteiro com 16 bits, com sinal na representação complementar de dois; de -32.768 a 32.767
	//int - um inteiro com 32 bits, com sinal na representação complementar de dois; de -2.147.483.648 a 2.147.483.647
	
	}
	
}
